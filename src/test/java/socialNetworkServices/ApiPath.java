package socialNetworkServices;

public class ApiPath {
	
	private static final String NEW_POST_PATH = "users";
	private static final String ADD_COMMENTS_PATH = "comments";
	private static final String GET_LIST_OF_USERS_PATH = "users";
	
	
	
	public  String getNewPost() {
		return NEW_POST_PATH;
	}
	public  String getAddComments() {
		return ADD_COMMENTS_PATH;
	}
	public  String getGetListOfUsers() {
		return GET_LIST_OF_USERS_PATH;
	}
	
	
	
	

}
